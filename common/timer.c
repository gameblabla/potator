#include "timer.h"
#include <stdlib.h>
#include <stdio.h>

static uint8 timer_regs[3];
static int32 timer_cycles;
static unsigned char   timer_activated;

////////////////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////////////////
//
//
//
//
//
////////////////////////////////////////////////////////////////////////////////
void timer_init()
{
}
////////////////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////////////////
//
//
//
//
//
////////////////////////////////////////////////////////////////////////////////
void timer_done()
{
	//fprintf(log_get(), "timer: done\n");
}
////////////////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////////////////
//
//
//
//
//
////////////////////////////////////////////////////////////////////////////////
void timer_reset()
{
	//fprintf(log_get(), "timer: reset\n");
	timer_regs[0]=0x00;
	timer_regs[1]=0x00;
	timer_regs[2]=0x00;

	timer_cycles = 0;
	timer_activated = 0;
}
////////////////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////////////////
//
//
//
//
//
////////////////////////////////////////////////////////////////////////////////
void timer_write(uint32 addr, uint8 data)
{
	timer_regs[addr&0x01] = data;

	if (addr&0x01)
	{
		timer_cycles = ((uint32)data)*256;
		timer_activated = 1;
		timer_regs[0]&=0xfe;
	}
}
////////////////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////////////////
//
//
//
//
//
////////////////////////////////////////////////////////////////////////////////
uint8 timer_read(uint32 addr)
{
	return(timer_regs[addr&0x01]);
}
////////////////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////////////////
//
//
//
//
//
////////////////////////////////////////////////////////////////////////////////
void timer_exec(uint32 cycles)
{
	if (timer_activated)
	{
		timer_cycles-=cycles;
		
		if (timer_cycles<0)
		{
			timer_regs[0]|=0x01;
			interrupts_irq();
			timer_activated=0;
		}
	}
}
